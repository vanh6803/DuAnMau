package com.example.anhnvph25971_duanmau.object;

public class Top {
    private String tenSach_top;
    private int soLuong_top;

    public Top() {
    }

    public Top(String tenSach_top, int soLuong_top) {
        this.tenSach_top = tenSach_top;
        this.soLuong_top = soLuong_top;
    }

    public String getTenSach_top() {
        return tenSach_top;
    }

    public void setTenSach_top(String tenSach_top) {
        this.tenSach_top = tenSach_top;
    }

    public int getSoLuong_top() {
        return soLuong_top;
    }

    public void setSoLuong_top(int soLuong_top) {
        this.soLuong_top = soLuong_top;
    }
}
